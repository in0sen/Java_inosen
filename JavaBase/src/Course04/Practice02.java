package Course04;

public class Practice02 {

	public static void main(String[] args) {

		String pr1 = "変数の練習問題";
		int pr2 = 100;
		int pr3 = 100 * 100;
		String pr4 = "Java" + "勉強中";

		System.out.println(pr1);
		System.out.println(pr2);
		System.out.println(pr3);
		System.out.println(pr4);

		int pr61 = 100;
		int pr62 = 200;
		int pr63 = pr61 + pr62;

		System.out.println(pr61);
		System.out.println(pr62);
		System.out.println(pr63);

		System.out.println("変数の勉強中です。" + pr61 + "+" + pr61 + "は「" + pr62 + "」です");
		//①～⑧すべて合ってた

	}

}
